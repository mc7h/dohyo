enum Hiragana: String, CaseIterable {
    // Vowels
    case a = "あ"
    case i = "い"
    case u = "う"
    case e = "え"
    case o = "お"
    
    // K-row
    case ka = "か"
    case ki = "き"
    case ku = "く"
    case ke = "け"
    case ko = "こ"
    
    // S-row
    case sa = "さ"
    case shi = "し"
    case su = "す"
    case se = "せ"
    case so = "そ"
    
    // T-row
    case ta = "た"
    case chi = "ち"
    case tsu = "つ"
    case te = "て"
    case to = "と"
    
    // N-row
    case na = "な"
    case ni = "に"
    case nu = "ぬ"
    case ne = "ね"
    case no = "の"
    
    // H-row
    case ha = "は"
    case hi = "ひ"
    case fu = "ふ"
    case he = "へ"
    case ho = "ほ"
    
    // M-row
    case ma = "ま"
    case mi = "み"
    case mu = "む"
    case me = "め"
    case mo = "も"
    
    // Y-row
    case ya = "や"
    case yu = "ゆ"
    case yo = "よ"
    
    // R-row
    case ra = "ら"
    case ri = "り"
    case ru = "る"
    case re = "れ"
    case ro = "ろ"
    
    // W-row
    case wa = "わ"
    case wo = "を"
    
    // N
    case n = "ん"
}
